exports.registerPatient = async (req, res) => {
  try {
    const { name, email, password, age, gender, contact } = req.body;

    // Check if user exists
    let patient = await Patient.findOne({ email });
    if (patient) return res.status(400).json({ msg: "User already exists" });

    // Create instance
    patient = new Patient({ name, email, password, age, gender, contact });

    // Hash Password
    const salt = await bcrypt.genSalt(10);
    patient.password = await bcrypt.hash(password, salt);

    await patient.save();

    // Generate Token
    const token = jwt.sign(
      { id: patient._id, role: 'patient' }, 
      process.env.JWT_SECRET, 
      { expiresIn: "7d" }
    );

    res.json({
      token,
      patient: {
        id: patient._id,
        name: patient.name,
        patientId: patient.patientId, // Auto-generated by your schema middleware
        email: patient.email,
        role: 'patient'
      }
    });
  } catch (err) {
    console.error("Registration Error:", err.message);
    res.status(500).send("Registration Error");
  }
};

